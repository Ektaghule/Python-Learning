#for loop is used for iterating over a sequence of a list, a tuple, a dictionary, a set, or a string.
fruit=["apple","orange","kiwi"]
for i in fruit:
    print(i)

#Loop Through a String
lang="Python"
for x in lang:
    print(x)

#for loop with range() unction
val=range(6)
for v in val:
    print(v)